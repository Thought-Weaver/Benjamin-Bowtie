class Photosynthesize(Ability):
    def __init__(self):
        super().__init__(
            icon="\u2600\uFE0F",
            name="Photosynthesize",
            class_key=ExpertiseClass.Fisher,
            description="Regenerate 5% of your health each turn for 2 turns.",
            flavor_text="",
            mana_cost=0,
            cooldown=8,
            num_targets=0,
            level_requirement=20,
            target_own_group=True,
            purchase_cost=0,
            scaling=[]
        )

    def use_ability(self, caster: Player | NPC, targets: List[Player | NPC]) -> str:
        buff = RegenerateHP(
            turns_remaining=2,
            value=0.05,
            source_str=self.get_icon_and_name()
        )

        result_str: str = "{0}" + f" used {self.get_icon_and_name()}!\n\n"
        results: List[str] = self._use_positive_status_effect_ability(caster, targets, [buff])
        result_str += "\n".join(results)

        caster.get_stats().dueling.fisher_abilities_used += 1

        return result_str

    def __getstate__(self):
        return self.__dict__

    def __setstate__(self, state: dict):
        self.__init__() # type: ignore


