class Item():
    def __init__(self, key: ItemKey, icon: str, name: str, value: int, rarity: Rarity, description: str, flavor_text:str, class_tags: List[ClassTag], state_tags: List[StateTag] | None=None, count=1, level_requirement=0, item_effects: ItemEffects | None=None, altering_item_keys: List[ItemKey] | None=None, attr_requirements: Attributes | None=None, armor_stats: ArmorStats | None=None, weapon_stats: WeaponStats | None=None, consumable_stats: ConsumableStats | None=None):
        self._key: ItemKey = key
        self._icon: str = icon
        self._name: str = name
        self._value: int = value
        self._rarity: Rarity = rarity
        self._description: str = description
        self._flavor_text: str = flavor_text
        self._class_tags: List[ClassTag] = class_tags
        self._state_tags: List[StateTag] = [] if state_tags is None else state_tags
        self._count: int = count
        self._level_requirement: int = level_requirement
        self._item_effects: ItemEffects | None = item_effects
        self._altering_item_keys: List[ItemKey] = [] if altering_item_keys is None else altering_item_keys
        self._attr_requirements: Attributes | None = attr_requirements 

        self._armor_stats: ArmorStats | None = armor_stats
        self._weapon_stats: WeaponStats | None = weapon_stats
        self._consumable_stats: ConsumableStats | None = consumable_stats

    @staticmethod
    def load_from_state(item_data: dict):
        armor_data = item_data.get("armor_stats")
        armor_stats = None
        if armor_data is not None:
            armor_stats = ArmorStats()
            armor_stats.__setstate__(armor_data)
        
        weapon_data = item_data.get("weapon_stats")
        weapon_stats = None
        if weapon_data is not None:
            weapon_stats = WeaponStats()
            weapon_stats.__setstate__(weapon_data)

        consumable_data = item_data.get("consumable_stats")
        consumable_stats = None
        if consumable_data is not None:
            consumable_stats = ConsumableStats()
            consumable_stats.__setstate__(consumable_data)

        item_effects_data = item_data.get("item_effects")
        item_effects = None
        if item_effects_data is not None:
            item_effects = ItemEffects([], [], [], [], [], [], [], [])
            item_effects.__setstate__(item_effects_data)

        attr_requirements_data: dict | None = item_data.get("attr_requirements")
        attr_requirements = None
        if attr_requirements_data is not None:
            attr_requirements = Attributes(
                attr_requirements_data.get("constitution", 0),
                attr_requirements_data.get("strength", 0),
                attr_requirements_data.get("dexterity", 0),
                attr_requirements_data.get("intelligence", 0),
                attr_requirements_data.get("luck", 0),
                attr_requirements_data.get("memory", 0)
            )

        return Item(
            item_data.get("key", ""),
            item_data.get("icon", ""),
            item_data.get("name", ""),
            item_data.get("value", 0),
            item_data.get("rarity", Rarity.Unknown),
            item_data.get("description", ""),
            item_data.get("flavor_text", ""),
            item_data.get("class_tags", []),
            item_data.get("state_tags", []),
            item_data.get("count", 1),
            item_data.get("level_requirement", 0),
            item_effects,
            item_data.get("altering_item_keys", []),
            attr_requirements,
            armor_stats,
            weapon_stats,
            consumable_stats
        )

    def remove_amount(self, amount: int):
        if amount <= self._count:
            result = Item(
                self._key,
                self._icon,
                self._name,
                self._value,
                self._rarity,
                self._description,
                self._flavor_text,
                self._class_tags,
                self._state_tags[:],
                amount,
                self._level_requirement,
                self._item_effects,
                self._altering_item_keys[:],
                self._attr_requirements,
                self._armor_stats,
                self._weapon_stats,
                self._consumable_stats)
            self._count -= amount
            return result
        return None

    def add_amount(self, amount: int):
        self._count += amount

    def meets_attr_requirements(self, attributes: Attributes):
        attr_reqs_met: bool = self._attr_requirements is None or (
            max(0, attributes.constitution) >= self._attr_requirements.constitution and
            max(0, attributes.strength) >= self._attr_requirements.strength and
            max(0, attributes.dexterity) >= self._attr_requirements.dexterity and
            max(0, attributes.intelligence) >= self._attr_requirements.intelligence and
            max(0, attributes.luck) >= self._attr_requirements.luck and
            max(0, attributes.memory) >= self._attr_requirements.memory
        )
        return attr_reqs_met

    def meets_requirements(self, level: int, attributes: Attributes):
        level_req_met: bool = level >= self._level_requirement
        return level_req_met and self.meets_attr_requirements(attributes)

    def get_name(self) -> str:
        return self._name

    def get_full_name(self) -> str:
        return f"{self._icon} {self._name}"

    def get_name_and_count(self) -> str:
        if self._count > 1:
            return f"{self._name} ({self._count})"
        return f"{self._name}"

    def get_full_name_and_count(self) -> str:
        if self._count > 1:
            return f"{self._icon} {self._name} ({self._count})"
        return f"{self._icon} {self._name}"

    def get_value(self) -> int:
        sockted_item_keys = list(filter(lambda x: x != "", self._altering_item_keys))
        value = self._value
        for item_key in sockted_item_keys:
            value += int(LOADED_ITEMS.get_item_state(item_key)["value"])
        return value

    def get_value_str(self) -> str:
        if self.get_value() == 1:
            return "1 coin"
        return f"{self.get_value()} coins"

    def get_count(self) -> int:
        return self._count

    def get_icon(self) -> str:
        return self._icon

    def get_rarity(self) -> Rarity:
        return self._rarity

    def get_description(self) -> str:
        return self._description
    
    def get_flavor_text(self) -> str:
        return self._flavor_text

